#include <stdio.h>
/**
 * op_add - Addition
 *
 * @a: left operand
 * @b: right operand
 *
 * Return: addtion
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracton function
 *
 * @a: left operand
 * @b: right operand
 *
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply function
 *
 * @a: left operand
 * @b: right operand
 *
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Division function
 *
 * @a: left operand
 * @b: right operand
 *
 * Return: Division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus function
 *
 * @a: left operand
 * @b: right operand
 *
 * Return: Modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
